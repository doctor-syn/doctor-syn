#[allow(non_camel_case_types)]
type fty = f64;

const RECIP_2PI: fty = 0.1591549430918953357688837633725143620345;

pub fn sin(arg: fty) -> fty {
    let scaled: fty = arg * RECIP_2PI;
    let x: fty = scaled - scaled.round();
    (-0.0000795978135564681619446994463825844449 as f64)
        .mul_add(x * x, 0.0011251039233483632093906670512638370694 as f64)
        .mul_add(x * x, -0.0120293093815837587083929079549229916291 as f64)
        .mul_add(x * x, 0.1042285941703196255136347927732329854039 as f64)
        .mul_add(x * x, -0.7181222077484850721256518873820977766730 as f64)
        .mul_add(x * x, 3.8199525744232107661057457125514942650559 as f64)
        .mul_add(x * x, -15.0946425760590780811041894566331393312522 as f64)
        .mul_add(x * x, 42.0586939448620164904399014801571486455824 as f64)
        .mul_add(x * x, -76.7058597530604003747392381683185548195638 as f64)
        .mul_add(x * x, 81.6052492760750400501824780624242989421231 as f64)
        .mul_add(x * x, -41.3417022403997601538762424010531941752307 as f64)
        .mul_add(x * x, 6.2831853071795864768497321650524941104931 as f64)
        * x
}

fn test_function<F: Fn(fty) -> fty>(
    test_name: &str,
    accurate_values: &[(fty, fty)],
    limit: fty,
    f: F,
) {
    let mut max_ref_error: fty = 0.0;
    let mut bad_ycalc: fty = 0.0;
    let mut bad_yref: fty = 0.0;
    let mut bad_x: fty = 0.0;
    for &(x, yref) in accurate_values {
        let ycalc = f(x);
        let eref = (ycalc - yref).abs();
        if eref > max_ref_error {
            max_ref_error = eref;
            bad_ycalc = ycalc;
            bad_yref = yref;
            bad_x = x;
        }
    }
    println!("{}:", test_name);
    println!("max_ref_error={:25.20}", max_ref_error);
    println!("x    ={:016x} {:25.20}", bad_x.to_bits(), bad_x);
    println!("ycalc={:016x} {:25.20}", bad_ycalc.to_bits(), bad_ycalc);
    println!("yref ={:016x} {:25.20}", bad_yref.to_bits(), bad_yref);
    assert!(!max_ref_error.is_nan());
    assert!(max_ref_error <= limit);
}

